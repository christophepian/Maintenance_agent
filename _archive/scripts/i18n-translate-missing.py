# Sort by length descending for greedy matching
DICT_SORTED = sorted(DICT.items(), key=lambda x: -len(x[0]))

def translate_value(val: str) -> str:
    """Try to translate a string value using the dictionary."""
    # Strip emoji prefix/suffix for matching
    stripped = re.sub(r'^[\U00010000-\U0010ffff\u2600-\u26ff\u2700-\u27bf\s]+', '', val).strip()
    stripped = re.sub(r'[\U00010000-\U0010ffff\u2600-\u26ff\u2700-\u27bf\s]+$', '', stripped).strip()
    
    # Try exact match first
    if stripped in DICT:
        prefix = val[:val.index(stripped)] if stripped in val else ''
        suffix = val[val.index(stripped)+len(stripped):] if stripped in val else ''
        return prefix + DICT[stripped] + suffix
    
    # Case-insensitive exact
    lower = stripped.lower()
    for en, fr in DICT_SORTED:
        if en.lower() == lower:
            prefix = val[:val.index(stripped)] if stripped in val else ''
            suffix = val[val.index(stripped)+len(stripped):] if stripped in val else ''
            return prefix + fr + suffix
    
    return val  # unchanged

def translate_json(en_obj: dict, fr_obj: dict) -> tuple[dict, int, int]:
    """Recursively translate fr_obj values where they equal en_obj values."""
    result = copy.deepcopy(fr_obj)
    translated = 0
    skipped = 0
    
    def recurse(en_node, fr_node, res_node):
        nonlocal translated, skipped
        for k in en_node:
            if k not in fr_node:
                continue
            en_v = en_node[k]
            fr_v = fr_node[k]
            if isinstance(en_v, dict) and isinstance(fr_v, dict):
                recurse(en_v, fr_v, res_node[k])
            elif isinstance(en_v, str) and isinstance(fr_v, str):
                if en_v == fr_v:  # untranslated
                    new_v = translate_value(en_v)
                    if new_v != en_v:
                        res_node[k] = new_v
                        translated += 1
                    else:
                        skipped += 1
    
    recurse(en_obj, fr_obj, result)
    return result, translated, skipped

namespaces = ['manager', 'owner', 'contractor', 'tenant', 'common']
total_translated = 0
total_skipped = 0

for ns in namespaces:
    en_path = BASE / 'en' / f'{ns}.json'
    fr_path = BASE / 'fr' / f'{ns}.json'
    if not en_path.exists() or not fr_path.exists():
        print(f"  {ns}: missing file, skipping")
        continue
    
    en_obj = json.loads(en_path.read_text())
    fr_obj = json.loads(fr_path.read_text())
    
    new_fr, t_count, s_count = translate_json(en_obj, fr_obj)
    
    fr_path.write_text(json.dumps(new_fr, ensure_ascii=False, indent=2) + '\n')
    print(f"  {ns}: translated={t_count}, still_untranslated={s_count}")
    total_translated += t_count
    total_skipped += s_count

print(f"\nTotal translated: {total_translated}, still needing work: {total_skipped}")
